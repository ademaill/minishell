#include "libft.h"

void	ft_skip_space(char *line, int *i)
{
	while (line[(*i)] == ' ' || line[(*i)] == '\n' || line[(*i)] == '\t'
		|| line[(*i)] == '\v' || line[(*i)] == '\f' || line[(*i)] == '\r')
		(*i)++;
}
