#include "libft.h"

void	ft_ignore_sep(char *line, int *i)
{
	if (line[(*i)] && line[(*i)] == '\\' && line[(*i) + 1]
		&& line[(*i) + 1] == ';')
		(*i)++;
	else if (line[(*i)] && line[(*i)] == '\\' && line[(*i) + 1]
		&& line[(*i) + 1] == '|')
		(*i)++;
	else if (line[(*i)] && line[(*i)] == '\\' && line[(*i) + 1]
		&& line[(*i) + 1] == '>')
		(*i)++;
	else if (line[(*i)] && line[(*i)] == '\\' && line[(*i) + 1]
		&& line[(*i) + 1] == '>' && line[(*i) + 2] && line[(*i) + 2] == '>')
		(*i)++;
}
