#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		size_str;
	char	*dup;
	int		i;

	size_str = ft_strlen(s);
	dup = malloc(sizeof(char) * (size_str + 1));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
